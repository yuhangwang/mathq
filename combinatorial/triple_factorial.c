////////////////////////////////////////////////////////////////////////////////
// double Triple_Factorial( int n )                                           //
//                                                                            //
//  Description:                                                              //
//     This function computes n!!! for -2 <= n <= Triple_Factorial_Max_Arg(). //
//     If n > Triple_Factorial_Max_Arg(), then DBL_MAX is returned and if     //
//     n < -2, then 0 is returned.                                            //
//                                                                            //
//  Arguments:                                                                //
//     int    n   Argument of the Triple Factorial function.                  //
//                                                                            //
//  Return Values:                                                            //
//     If n < -2, then 0 is returned.  If n > Triple_Factorial_Max_Arg(),     //
//     then DBL_MAX is returned.  If -2 <= n <= Triple_Factorial_Max_Arg(),   //
//     then n!!! is returned.                                                 //
//                                                                            //
//  Example:                                                                  //
//     int n;                                                                 //
//     double x;                                                              //
//                                                                            //
//     x = Triple_Factorial( n );                                             //
////////////////////////////////////////////////////////////////////////////////
double Triple_Factorial(int n) {

               // For an argument n < -2, return 0.0 //

   if ( n < -2 ) return 0.0;


           // For a large postive argument (n >= N) return DBL_MAX //

   if ( n >= N ) return DBL_MAX;

                         // Otherwise return n!!!. //

   if (n <= 0) return 1.0;
   return (double) triple_factorials[n];
}


